const { mkdirSync, writeFileSync } = require("node:fs");
const path = require("node:path");
const licenseChecker = require("license-checker");

const rootDir = path.resolve(__dirname, "..");
const outputPath = path.join(
  rootDir,
  "src",
  "data",
  "generated",
  "open-source-notices.ts",
);

const checkLicenses = () =>
  new Promise((resolve, reject) => {
    licenseChecker.init(
      {
        start: rootDir,
        production: true,
        excludePrivatePackages: true,
        customFormat: {
          repository: "",
          licenses: "",
          licenseText: "",
        },
      },
      (error, packages) => {
        if (error) {
          reject(error);
          return;
        }

        resolve(packages);
      },
    );
  });

const getPackageFromKey = (packageKey) => {
  const versionSeparatorIndex = packageKey.lastIndexOf("@");

  if (versionSeparatorIndex <= 0) {
    return { name: packageKey, version: "UNKNOWN" };
  }

  return {
    name: packageKey.slice(0, versionSeparatorIndex),
    version: packageKey.slice(versionSeparatorIndex + 1),
  };
};

checkLicenses().then((packages) => {
  const notices = Object.entries(packages)
    .map(([packageKey, packageInfo]) => {
      const { name, version } = getPackageFromKey(packageKey);

      return {
        name,
        version,
        license: Array.isArray(packageInfo.licenses)
          ? packageInfo.licenses.join(", ")
          : packageInfo.licenses || "UNKNOWN",
        licenseText: packageInfo.licenseText || "",
        repository: packageInfo.repository || undefined,
      };
    })
    .sort((a, b) => a.name.localeCompare(b.name));

  const source = `// This file is generated by scripts/generate-open-source-notices.js.
// Do not edit it by hand.

export type OpenSourceNotice = {
  name: string;
  version: string;
  license: string;
  licenseText: string;
  repository?: string;
};

export const openSourceNotices = ${JSON.stringify(notices, null, 2)} satisfies OpenSourceNotice[];
`;

  mkdirSync(path.dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, source);
  console.log(`Generated ${notices.length} open source notices.`);
});
