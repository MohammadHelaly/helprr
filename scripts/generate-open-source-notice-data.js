const getLicenseMetadataFallback = (license, repository, subject = "package") =>
  [
    `This ${subject} declares license: ${license}.`,
    subject === "package"
      ? "The installed npm package did not include license text."
      : "The source metadata did not include license text.",
    `Repository: ${repository || "Unavailable"}`,
  ].join("\n");

const isUsablePackageLicenseText = (packageInfo) => {
  const licenseText = packageInfo.licenseText || "";

  if (!licenseText.trim()) {
    return false;
  }

  return !readmeFilePattern.test(packageInfo.licenseFile || "");
};

const getPackageLicenseText = (packageName, packageInfo, license) => {
  if (isUsablePackageLicenseText(packageInfo)) {
    return packageInfo.licenseText;
  }

  return getLicenseMetadataFallback(license, packageInfo.repository);
};

const checkLicenses = () =>
  new Promise((resolve, reject) => {
    licenseChecker.init(
      {
        start: rootDir,
        production: true,
        excludePrivatePackages: true,
        customFormat: {
          repository: "",
          licenses: "",
          licenseFile: "",
          licenseText: "",
        },
      },
      (error, packages) => {
        if (error) {
          reject(error);
          return;
        }

        resolve(packages);
      },
    );
  });

const getPackageFromKey = (packageKey) => {
  const versionSeparatorIndex = packageKey.lastIndexOf("@");

  if (versionSeparatorIndex <= 0) {
    return { name: packageKey, version: "UNKNOWN" };
  }

  return {
    name: packageKey.slice(0, versionSeparatorIndex),
    version: packageKey.slice(versionSeparatorIndex + 1),
  };
};

const main = async () => {
  const packages = await checkLicenses();
  const packageNotices = Object.entries(packages).map(
    ([packageKey, packageInfo]) => {
      const { name, version } = getPackageFromKey(packageKey);
      const license = Array.isArray(packageInfo.licenses)
        ? packageInfo.licenses.join(", ")
        : packageInfo.licenses || "UNKNOWN";

      return {
        name,
        version,
        license,
        licenseText: getPackageLicenseText(name, packageInfo, license),
        repository: packageInfo.repository || undefined,
      };
    },
  );

  const notices = [...packageNotices, ...manualNotices].sort((a, b) =>
    a.name.localeCompare(b.name),
  );

  const source = `// This file is generated by scripts/generate-open-source-notice-data.js.
// Do not edit it by hand.

export type OpenSourceNotice = {
  name: string;
  version: string;
  license: string;
  licenseText: string;
  repository?: string;
};

export const openSourceNotices = ${JSON.stringify(notices, null, 2)} satisfies OpenSourceNotice[];
`;
  const formattedSource = await prettier.format(source, {
    parser: "typescript",
  });

  mkdirSync(path.dirname(outputPath), { recursive: true });
  writeFileSync(outputPath, formattedSource);
  console.log(`Generated ${notices.length} open source notices.`);
};

main();
